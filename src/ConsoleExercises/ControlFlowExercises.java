public class ControlFlowExercises {
    public static void main(String[] args) {

        int i = 0;
        do {
            System.out.println(i);
            i+=2;
        }while (i <= 100);

//first part of exercise//

        i = 100;
        do {
            System.out.println(i);
            i-=5;
        }while (i >= -10);

    }


}
